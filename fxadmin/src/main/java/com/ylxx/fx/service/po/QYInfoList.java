package com.ylxx.fx.service.po;

public class QYInfoList {
	private String ogcd;// 机构号
	private String ognm;// 签约机构名称
	private String accDate;// 日期
	private String nowTime;// 时间
	private String qydt;// 累计签约人数
	public String getOgcd() {
		return ogcd;
	}
	public void setOgcd(String ogcd) {
		this.ogcd = ogcd;
	}
	public String getOgnm() {
		return ognm;
	}
	public void setOgnm(String ognm) {
		this.ognm = ognm;
	}
	public String getAccDate() {
		return accDate;
	}
	public void setAccDate(String accDate) {
		this.accDate = accDate;
	}
	public String getNowTime() {
		return nowTime;
	}
	public void setNowTime(String nowTime) {
		this.nowTime = nowTime;
	}
	public String getQydt() {
		return qydt;
	}
	public void setQydt(String qydt) {
		this.qydt = qydt;
	}
	
}
